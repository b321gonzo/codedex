#add 2 numbers a and b
def add(a,b):
    sum = a+b
    return(sum)

#subtract 2 numbers a and b
def subtract(a,b):
    difference = a-b
    return(difference)

#multiply 2 numbers a and b
def multiply(a,b):
    product = a*b
    return(product)

#divide number a by number b
def divide(a,b):
    quotient = a/b
    return(quotient)

#take number a to the power of number b
def exp(a,b):
    power = a**b
    return(power)

print(add(6,2))
print(subtract(6,2))
print(multiply(6,2))
print(divide(6,2))
print(exp(6,2))
